//Write a program to demonstrate the use of different data types.
class DataType
{
public static void main(String []args){

//integer types
byte bvar=127;
short shortvar=32000;
int intvar=100000;
long longvar=10000000000L;

//floating points
float fvar=3.14f;
double dvar=3.141492653689793;

//character type
char cvar='V';

//String
String svar="hello";

//boolean values
boolean btrue=true;
boolean bfalse=false;

System.out.println("byte = "+bvar);
System.out.println("short = "+shortvar);
System.out.println("integer = "+intvar);
System.out.println("long = "+longvar);
System.out.println("float = "+fvar);
System.out.println("double = "+dvar);
System.out.println("character = "+cvar);
System.out.println("string = "+svar);
System.out.println("boolean = "+btrue);
System.out.println("booleab = "+bfalse);

}
}